local function move(level, key)
    if key == 'up' or key == 'down' or key == 'left' or key == 'right' then

        local y, x
        for testY, row in ipairs(level) do
            for testX, cell in ipairs(row) do
                if cell == player or cell == playerOnStorage then
                    y, x = testY, testX
                end
            end
        end

        local dy, dx
        if key == 'left' then
            dy, dx = 0, -1
        elseif key == 'right' then
            dy, dx = 0, 1
        elseif key == 'up' then
            dy, dx = -1, 0
        elseif key == 'down' then
            dy, dx = 1, 0
        end

        local current = level[y][x]
        local adjacent = level[y + dy][x + dx]
        local beyond
        if level[y + dy + dy] then
            beyond = level[y + dy + dy][x + dx + dx]
        end

        local nextCurrent = {
            [player] = empty,
            [playerOnStorage] = storage,
        }

        local nextAdjacentMove = {
            [empty] = player,
            [storage] = playerOnStorage,
        }

        local nextAdjacentPush = {
            [box] = player,
            [boxOnStorage] = playerOnStorage,
        }

        local nextBeyond = {
            [empty] = box,
            [storage] = boxOnStorage,
        }

        if nextAdjacentMove[adjacent] then
            level[y][x] = nextCurrent[current]
            level[y + dy][x + dx] = nextAdjacentMove[adjacent]
        elseif nextAdjacentPush[adjacent] and nextBeyond[beyond] then
            level[y][x] = nextCurrent[current]
            level[y + dy][x + dx] = nextAdjacentPush[adjacent]
            level[y + dy + dy][x + dx + dx] = nextBeyond[beyond]
        end

        local complete = true

        for y, row in ipairs(level) do
            for x, cell in ipairs(row) do
                if cell == box then
                    complete = false
                end
            end
        end

        if complete then
            currentLevel = currentLevel + 1
            if currentLevel > #levels then
                currentLevel = 1
            end
            loadLevel(1)
        end

    elseif key == 'r' then
        loadLevel(1)

    elseif key == 'p' then
        currentLevel = currentLevel - 1
        if currentLevel < 1 then
            currentLevel = #levels
        end
        loadLevel(1)

    elseif key == 'n' then
        currentLevel = currentLevel + 1
        if currentLevel > #levels then
            currentLevel = 1
        end
        loadLevel(1)
    end
end

